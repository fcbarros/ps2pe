#ifndef __EMU_FIFO_H__
#define __EMU_FIFO_H__

#include "EmuMemory.h"


////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////
// Defines
////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////

// Base Address
#define EMU_FIFO_START_ADDR     0x10004000
#define EMU_FIFO_END_ADDR       0x10007FFF

#define EMU_FIFO_VIF0           0x10004000
#define EMU_FIFO_VIF1           0x10005000
#define EMU_FIFO_GIF            0x10006000
#define EMU_FIFO_OUT            0x10007000
#define EMU_FIFO_IN             0x10007010


////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////
// Structs
////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////

typedef struct
{
    EMU_U32 Data;
    EMU_U08 null0[12];
} stEmu_Fifo_Control_Regs;


/////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////
// Functions
/////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////

// Initializes the Fifo memory pointers and registers
void Emu_Fifo_Init( void );
// Resets registers
void Emu_Fifo_Reset( void );
// Callback for Fifo access
void Emu_Fifo_callback( EMU_U32 Address );

EMU_U08 * Emu_Fifo_GetPointer( EMU_U32 Address );

#endif
