#ifndef __EMU_IPU_H__
#define __EMU_IPU_H__

#include "EmuMemory.h"

////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////
// Defines
////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////

// Base Address
#define EMU_IPU_START_ADDR      0x10002000
#define EMU_IPU_END_ADDR        0x10002FFF

// Control Register Address
#define EMU_IPU_CMD             0x00000000
#define EMU_IPU_CTRL            0x00000010
#define EMU_IPU_BP              0x00000020
#define EMU_IPU_TOP             0x00000030


////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////
// Structs
////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////

typedef struct
{
    EMU_U32 CMD;
    EMU_U08 null0[12];
    EMU_U32 CTRL;
    EMU_U08 null1[12];
    EMU_U32 BP;
    EMU_U08 null2[12];
    EMU_U32 TOP;
    EMU_U08 null3[12];
} stEmu_Ipu_Control_Regs;


/////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////
// Functions
/////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////

// Initilizes IPU memory pointers and registers
void Emu_Ipu_Init( void );
// Resets IPU registers
void Emu_Ipu_Reset( void );
// Callback for IPU access
void Emu_Ipu_callback( EMU_U32 Address );

EMU_U08 * Emu_Ipu_GetPointer( EMU_U32 Address );

#endif
