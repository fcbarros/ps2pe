#ifndef __EMU_TIMER_H__
#define __EMU_TIMER_H__

#include "EmuMemory.h"

////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////
// Defines
////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////

#define EMU_TIMER_START_ADDR    0x10000000
#define EMU_TIMER_END_ADDR      0x10001FFF

// Base Address
#define EMU_TIMER0              0x10000000
#define EMU_TIMER1              0x10000800
#define EMU_TIMER2              0x10001000
#define EMU_TIMER3              0x10001800

// Control Register Address
#define EMU_TIMER_COUNT         0x00000000
#define EMU_TIMER_MODE          0x00000010
#define EMU_TIMER_COMP          0x00000020
#define EMU_TIMER_HOLD          0x00000030


////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////
// Structs
////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////

typedef struct
{
    EMU_U32 COUNT;
    EMU_U08 null0[12];
    EMU_U32 MODE;
    EMU_U08 null1[12];
    EMU_U32 COMP;
    EMU_U08 null2[12];
    EMU_U32 HOLD;
    EMU_U08 null3[12];
} stEmu_Timer_Control_Regs;


/////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////
// Functions
/////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////

// Initializes Timer memory pointers and registers
void Emu_Timer_Init( void );
// Resets Timer registers
void Emu_Timer_Reset( void );
// Callback for Timer access
void Emu_Timer_callback( EMU_U32 Address );

EMU_U08 * Emu_Timer_GetPointer( EMU_U32 Address );

#endif
